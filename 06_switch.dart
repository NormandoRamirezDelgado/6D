void main() {
  String dia = 'sábado';

  switch (dia) {
    case 'lunes':
      print('Hoy es lunes');
      break;
    case 'martes':
      print('Hoy es martes');
      break;
    case 'miércoles':
      print('Hoy es miércoles');
      break;
    default:
      print('Día no reconocido');
  }
}
